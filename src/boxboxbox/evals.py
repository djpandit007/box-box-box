"""Evaluation and dataset utilities for Phoenix observability.

Usage:
    python -m boxboxbox.evals                          # Run evaluators on recent spans
    python -m boxboxbox.evals --create-dataset 12345   # Create dataset from session spans
"""

from __future__ import annotations

import argparse
import logging
import re

import pandas as pd
from phoenix.client import Client
from phoenix.client.resources.spans import SpanQuery
from phoenix.evals import (
    ClassificationEvaluator,
    LLM,
    bind_evaluator,
    create_classifier,
    evaluate_dataframe,
)

from boxboxbox.config import settings

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Code-based evaluators (no LLM needed)
# ---------------------------------------------------------------------------

_LEADING_SIGNOFF_RE = re.compile(
    r"Lap \d+/\d+ \| Leading: [A-Z]{3} \| [A-Z]{3} \| [A-Z]{3}$",
    re.MULTILINE,
)

_RACING_ACTION_TERMS = re.compile(
    r"\b(overtakes?|pits?|undercut|overcut|DRS|lunge[sd]?|dive[sd]?|charges?|fires?)\b",
    re.IGNORECASE,
)


def check_leading_signoff(output: str) -> bool:
    """Race summaries should end with 'Lap X/Y | Leading: ACR | ACR | ACR'."""
    return bool(_LEADING_SIGNOFF_RE.search(output))


def check_no_hallucinated_racing(output: str) -> bool:
    """Pre/post-session summaries should NOT contain racing action terms."""
    return not bool(_RACING_ACTION_TERMS.search(output))


def check_dialogue_line_count(output: str, has_historian: bool = False) -> bool:
    """Digest should have 22-26 lines with Historian, 18-22 without."""
    lines = [
        line.strip()
        for line in output.strip().splitlines()
        if line.strip().startswith(("Lead:", "Analyst:", "Historian:"))
    ]
    count = len(lines)
    if has_historian:
        return 22 <= count <= 26
    return 18 <= count <= 22


def check_historian_consistency(output: str, prompt: str) -> bool:
    """Historian lines should only appear when historical context was provided."""
    has_context = "<weekend_context>" in prompt or "<historical_parallels" in prompt
    has_historian_lines = "Historian:" in output
    if has_historian_lines and not has_context:
        return False  # Historian spoke without historical data
    return True


def check_web_search_attribution(output: str) -> bool:
    """Digest should not contain raw URLs."""
    url_pattern = re.compile(r"https?://\S+")
    return not bool(url_pattern.search(output))


# ---------------------------------------------------------------------------
# LLM-as-judge evaluators
# ---------------------------------------------------------------------------

_SUMMARY_QUALITY_TEMPLATE = """\
You are evaluating a 60-second Formula 1 race summary generated by an AI commentator.

The summary was generated from this structured data prompt:
{input}

The generated summary is:
{output}

Evaluate the summary on these criteria:
1. FACTUAL ACCURACY: Does the summary only mention events present in the data? No hallucinations.
2. APPROPRIATE TONE: Is it energetic and punchy, like live commentary?
3. CONCISENESS: Is it 4-5 lines, ~80 words max?

Respond with a single label:
- "good" if all criteria are met
- "bad" if any criterion is violated"""

_DIGEST_QUALITY_TEMPLATE = """\
You are evaluating a post-session Formula 1 audio digest generated by an AI.

The digest is:
{output}

Evaluate on these criteria:
1. FORMAT: Each line starts with "Lead: ", "Analyst: ", or "Historian: " followed by an emotional tag in brackets
2. EMOTIONAL TAGS: Tags are from [dramatic], [excited], [analytical], [reflective], [warmly], [sighing], [laughing], [cautiously]
3. NATURAL FLOW: The dialogue reads naturally as spoken commentary
4. STANDINGS: The second-to-last line should be Analyst with a conversational standings walkthrough

Respond with a single label:
- "good" if all criteria are met
- "bad" if any criterion is violated"""


def _get_eval_model() -> LLM:
    """Create an OpenAI-compatible model for LLM-as-judge evals via OpenRouter."""
    return LLM(
        provider="openai",
        model="openai/gpt-4o-mini",
        api_key=settings.OPENROUTER_API_KEY,
        base_url="https://openrouter.ai/api/v1",
    )


def _build_summary_evaluator() -> ClassificationEvaluator:
    """Build the LLM-as-judge classifier for summary quality."""
    model = _get_eval_model()
    evaluator = create_classifier(
        name="summary_quality",
        prompt_template=_SUMMARY_QUALITY_TEMPLATE,
        llm=model,
        choices={"good": (1.0, "All criteria met"), "bad": (0.0, "One or more criteria violated")},
    )
    return bind_evaluator(evaluator, input_mapping={"input": "input", "output": "output"})


def _build_digest_evaluator() -> ClassificationEvaluator:
    """Build the LLM-as-judge classifier for digest quality."""
    model = _get_eval_model()
    evaluator = create_classifier(
        name="digest_quality",
        prompt_template=_DIGEST_QUALITY_TEMPLATE,
        llm=model,
        choices={"good": (1.0, "All criteria met"), "bad": (0.0, "One or more criteria violated")},
    )
    return bind_evaluator(evaluator, input_mapping={"output": "output"})


# ---------------------------------------------------------------------------
# Span fetching and evaluation
# ---------------------------------------------------------------------------


def _fetch_spans(client: Client, span_name: str) -> pd.DataFrame | None:
    """Fetch spans by name from Phoenix."""
    query = SpanQuery().where(f'name == "{span_name}"')
    df = client.spans.get_spans_dataframe(
        project_identifier=settings.PHOENIX_PROJECT_NAME,
        query=query,
    )
    if df is None or df.empty:
        return None
    return df


def run_evaluations() -> None:
    """Fetch recent spans from Phoenix and run evaluators."""
    client = Client()

    # Fetch summary spans
    summary_spans = _fetch_spans(client, "summarise_window")

    if summary_spans is not None:
        logger.info("Evaluating %d summary spans", len(summary_spans))

        # Code-based evals
        results = []
        for _, row in summary_spans.iterrows():
            output = row.get("attributes.output.value", "")
            input_val = row.get("attributes.input.value", "")
            results.append(
                {
                    "span_id": row.name,
                    "has_signoff": check_leading_signoff(str(output)),
                    "no_hallucinated_racing": check_no_hallucinated_racing(str(output))
                    if "[pre-session]" in str(input_val) or "[post-session]" in str(input_val)
                    else None,
                }
            )
        results_df = pd.DataFrame(results)
        logger.info("Summary code-based eval results:\n%s", results_df.describe())

        # LLM-as-judge eval
        try:
            evaluator = _build_summary_evaluator()
            eval_df = summary_spans.copy()
            eval_df["input"] = eval_df.get("attributes.input.value", "")
            eval_df["output"] = eval_df.get("attributes.output.value", "")
            llm_results = evaluate_dataframe(
                dataframe=eval_df,
                evaluators=[evaluator],
            )
            logger.info("Summary LLM eval results:\n%s", llm_results.describe())
        except Exception:
            logger.exception("LLM-as-judge evaluation failed for summaries")
    else:
        logger.info("No summary spans found to evaluate")

    # Fetch digest spans
    digest_spans = _fetch_spans(client, "generate_digest")

    if digest_spans is not None:
        logger.info("Evaluating %d digest spans", len(digest_spans))

        # Code-based evals
        for _, row in digest_spans.iterrows():
            output = str(row.get("attributes.output.value", ""))
            input_val = str(row.get("attributes.input.value", ""))
            has_historian = "Historian:" in output
            logger.info(
                "Digest line count OK: %s, Historian consistent: %s, No raw URLs: %s",
                check_dialogue_line_count(output, has_historian),
                check_historian_consistency(output, input_val),
                check_web_search_attribution(output),
            )

        # LLM-as-judge eval
        try:
            evaluator = _build_digest_evaluator()
            eval_df = digest_spans.copy()
            eval_df["output"] = eval_df.get("attributes.output.value", "")
            llm_results = evaluate_dataframe(
                dataframe=eval_df,
                evaluators=[evaluator],
            )
            logger.info("Digest LLM eval results:\n%s", llm_results.describe())
        except Exception:
            logger.exception("LLM-as-judge evaluation failed for digests")
    else:
        logger.info("No digest spans found to evaluate")


# ---------------------------------------------------------------------------
# Dataset creation from traces
# ---------------------------------------------------------------------------


def create_session_dataset(session_key: int) -> None:
    """Export all summary + digest spans from a session into a Phoenix dataset."""
    client = Client()

    query = SpanQuery().where(
        f'attributes.session_key == "{session_key}" and name in ["summarise_window", "generate_digest"]'
    )
    spans = client.spans.get_spans_dataframe(
        project_identifier=settings.PHOENIX_PROJECT_NAME,
        query=query,
    )

    if spans is None or spans.empty:
        logger.warning("No spans found for session %d", session_key)
        return

    dataset_name = f"session-{session_key}"
    inputs = []
    outputs = []
    metadata_list = []
    for _, row in spans.iterrows():
        inputs.append({"value": str(row.get("attributes.input.value", ""))})
        outputs.append({"value": str(row.get("attributes.output.value", ""))})
        metadata_list.append(
            {
                "session_key": str(session_key),
                "span_name": str(row.get("name", "")),
                "session_type": str(row.get("attributes.session_type", "")),
            }
        )

    dataset = client.datasets.create_dataset(
        name=dataset_name,
        dataset_description=f"Spans from session {session_key}",
    )
    client.datasets.add_examples_to_dataset(
        dataset=dataset,
        inputs=inputs,
        outputs=outputs,
        metadata=metadata_list,
    )

    logger.info("Created dataset '%s' with %d examples", dataset_name, len(inputs))


# ---------------------------------------------------------------------------
# CLI entry point
# ---------------------------------------------------------------------------


def main() -> None:
    logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")

    parser = argparse.ArgumentParser(description="Run evaluations or create datasets from Phoenix traces")
    parser.add_argument(
        "--create-dataset",
        type=int,
        metavar="SESSION_KEY",
        help="Create a Phoenix dataset from all spans of the given session",
    )
    args = parser.parse_args()

    if args.create_dataset:
        create_session_dataset(args.create_dataset)
    else:
        run_evaluations()


if __name__ == "__main__":
    main()
